'''3)- Ler dois valores numéricos e exibir a diferença do maior para o menor.'''

a = int(input("Insira o primeiro número:"))
b = int(input("Insira o segundo número: "))

if a>b:
 print(f"O módulo da diferença do primeiro primeiro e segundo número é {a - b}")
else:
 print(f"O módulo da diferença do primeiro e segundo número é {b - a}")