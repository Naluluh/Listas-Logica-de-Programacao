"""11)- Ler 5 números inteiros e identificar o maior e o menor valor, exibir os
mesmos."""

a = int(input("Insira o 1° número: "))
b = int(input("Insira o 2° número: "))
c = int(input("Insira o 3° número: "))
d = int(input("Insira o 4° número: "))
e = int(input("Insira o 5° número: "))

print(f"O maior número digitado foi: {max(a,b,c,d,e)}")
print(f"O menor número digitado foi: {min(a,b,c,d,e)}\n")